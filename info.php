 Структура бізнес-плану може бути представлена такими пунктами:
1. Титульний лист;
2. Резюме;
3. Мета проекту;
4. Аналіз ринку в певній сфері;
5. Положення підприємства в галузі;
6. Опис продукту;
7. Маркетинг план по збуту продукції;
8. План виробництва;
9. Організаційний план;
10. Фінансове планування;
11. Аналіз ризику;
Розкриття найважливіших пунктів:
Положення підприємства в галузі
▪ усі види діяльності підприємства, його продукція(послуги),
споживачі(клієнти);
▪ організаційно-правова форма підприємства, організаційна структура,
засновники, партнери та персонал;
▪ фінансово-економічні показники діяльності підприємства (фірми);
▪ розташування підприємства (адреса, характеристика будівлі або
приміщення, власність або оренда, необхідність реконструкції);
▪ специфіка роботи (сезонність, час роботи (години та дні тижня) та інші
особливості, пов'язані зі сферою бізнесу або використовуваними ресурсами).17
Опис продукту
Орієнтовна структура розділу:
● найменування продукції (послуги);
● призначення та область застосування;
● опис і основні характеристики;
● конкурентоспроможність продукції (послуги) з виділенням параметрів,
за якими продукт перевершує конкурентів або поступається;
● патентоспроможність і авторські права;
● наявність або необхідність ліцензування продукції;
● наявність сертифіката якості продукції;
План виробництва
У цьому розділі здійснюється розрахунок виробничих витрат на планований
обсяг збуту: розрахунок собівартості продукції, складання кошторису витрат на
виробництво, визначення змінних і постійних витрат в собівартості. Орієнтовна
структура розділу така:
● загальні відомості про виробничі структури підприємства, наявності
необхідних транспортних зв'язків, інженерних мереж (електроенергія, вода, тепло,
каналізація, зв'язок та ін.), ресурсів, близькість до ринку збуту;
● використовувана технологія і рівень її освоєння підприємством;
● потреба в виробничих площах:
● потреба в виробничому персоналі (чисельність, спеціалізація,
кваліфікація);
● задоволення вимог щодо забезпечення екологічності виробництва для
навколишнього середовища і безпеки працюючих;
● готівкові та необхідні виробничі потужності;
● характеристика обладнання;18
● планування джерел постачання сировини, матеріалів, обладнання та
робітничих кадрів;
● визначення собівартості всіх видів продукції;
● кошторис поточних витрат на виробництво;
● аналіз структури собівартості.
Фінансове планування
В цьому розділі плануються інвестиційні вкладення (на будівельномонтажні роботи, придбання обладнання, створення оборотних коштів та ін.).
Обираються джерела фінансування проекту. Наводиться схема повернення
кредитних коштів.
Завершуватися розділ фінансового планування має аналізом ефективності
проекту. Для цих цілей можна використовувати методику проектного аналізу,
методи економічного аналізу фінансово-господарської діяльності
(рентабельність, фінансова стійкість проекту тощо).
Орієнтовна структура розділу така:
● звіт про прибутки та збитки за кожен рік терміну реалізації проекту,
(перший рік помісячно);
● структура податкових платежів;
● план руху грошових коштів першого року;
● плановий баланс першого року;
● потреба в інвестиціях;
● витрати, пов'язані з обслуговуванням кредиту (лізингу) (можуть
бути проаналізовані окремо);
● аналіз ефективності проекту.
